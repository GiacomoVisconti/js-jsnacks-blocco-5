const names = ['Edoardo', 'Simone', 'Francesco'];

// Stampa in console tutti i nomi

//Col ciclo for EACH
names.forEach((element) => {
    console.log(element);
    
})

//Col ciclo FOR

for (let i = 0; i < names.length; i++) {
    const element = names[i];
    
    console.log(element);
    
}

